  public static JSONObject createKeyEvent(String keyString, boolean pressed) {
    JSONObject obj = new JSONObject();
    try {
      obj.put("type", "KEY");
      obj.put("keylist", keyString);
      obj.put("pressed", Boolean.toString(pressed));
    } catch (Exception e) {
      Log.d(TAG,e.getMessage());
    }
    return obj;
  }

  public static JSONObject createMouseEvent(float dx, float dy, boolean absRel) {
    JSONObject obj = new JSONObject();
    try {
      obj.put("type", "MOUSE");
      obj.put("absrel", absRel ? "ABS" : "REL");
      obj.put(absRel ? "x" : "dx", dx);
      obj.put(absRel ? "y" : "dy", dy);
    } catch (Exception e) {
      Log.d(TAG,e.getMessage());
    }
    return obj;
  }
